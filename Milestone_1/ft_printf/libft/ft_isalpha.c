#include <unistd.h>

int	ft_isalpha(int c)
{
	int	istrue;

	istrue = 0;
	if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
		istrue = 1;
	return (istrue);
}
